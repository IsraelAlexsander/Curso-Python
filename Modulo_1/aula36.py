"""
Operadores de atribuição
= += -= *= /= //= **= %=
"""

contador = 0
print(contador)

contador += 2
print(contador)

contador -= 1
print(contador)

contador *= 10
print(contador)

contador /= 2
print(contador)

contador //= 2
print(contador)

contador **= 4
print(contador)

contador %= 2
print(contador)